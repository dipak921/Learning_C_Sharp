using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Revision_start
{
    internal class Jump_Statements
    {
        static void Main(string[] args)
        {
            // Jump statements in C# are used to alter the flow of control in a program. 
            // They allow you to jump to a different part of the code based on certain conditions or logic. 
            // The main jump statements in C# are:
            // 1. break: Exits the current loop or switch statement.
            // 2. continue: Skips the current iteration of a loop and moves to the next iteration.
            // 3. return: Exits from a method and optionally returns a value.
            // 4. goto: Jumps to a labeled statement within the same method (use with caution).
            // 5. throw: Used to signal an exception.
            Console.WriteLine("Jump Statements in C#");
            Console.WriteLine("1. break");
            Console.WriteLine("2. continue");
            Console.WriteLine("3. return");
            Console.WriteLine("4. goto");
            Console.WriteLine("5. throw");



        }
    }
}
